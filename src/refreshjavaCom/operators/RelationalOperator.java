package refreshjavaCom.operators;

public class RelationalOperator {
    //conditional operators
    public static void main(String[] args) {
        int num1 = 3;
        int num2 = 5;
        if(num1 == num2)
            System.out.println("num1 == num2");
        if(num1 != num2)
            System.out.println("num1 != num2");
        if(num1 > num2)
            System.out.println("num1 > num2");
        if(num1 >= num2)
            System.out.println("num1 >= num2");
        if(num1 < num2)
            System.out.println("num1 < num2");
        if(num1 <= num2)
            System.out.println("num1 <= num2");
    }
}