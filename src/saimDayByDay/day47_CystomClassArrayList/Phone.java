package saimDayByDay.day47_CystomClassArrayList;

public class Phone {


    String name;
    String brand;
    double version;
    int memory;

    public Phone(String name){
        this.name = name;
    }

    public Phone(String name, String brand){
        this.name = name;
        this.brand = brand;
    }

    public Phone(String name, String brand, double version){
        this.name = name;
        this.brand = brand;
        this.version = version;
    }

    public Phone(String name, String brand, double version, int memory){
        this.name = name;
        this.brand = brand;
        this.version = version;
        this.memory = memory;
    }



}