package javaTutorial.refreshjavaCom.ProgramExample;

class EvenOddProgram
{
    public static void main(String [] args)
    {
        int num1 = 22;
        if(num1 % 2 == 0)
        {
            System.out.println(num1+ " is an even number");
        }
        else
        {
            System.out.println(num1+ " is an odd number");
        }
    }
}
